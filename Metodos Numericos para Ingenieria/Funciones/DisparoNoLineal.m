function [y,yp,k] = DisparoNoLineal(a,b,alpha,beta,N,tol,M,f)
h = (b-a)/NM;
k = 0; 
TK = (beta - alpha)/(b - a);

while k <= M
    w(1,1) = alpha;
    w(2,1) = TK;
    u(1) = 0;
    u(2) = 1;
    for i = 2:N+1;
        x = a + (i-1)*h;
        k(1,1) = h*w(2,i-1);
        k(1,2) = h*f(x,w(1,i-1),w(2,i-1));
        k(2,1) = h*(w(2,i-1) + (1/2)*k(1,2));
        k(2,2) = h*f(x + h/2, w(1,i-1) + (1/2)*k(1,1), w(2,i-1) + (1/2)*k(1,2));
        k(3,1) = h*(w(2,i-1) + (1/2)*k(2,2));
        k(3,2) = h*f(k+h/2, w(1.i-1)+(1/2)*k(2,1), w(2,i-1)+(1/2)*k(2,2));
        k(4,1) = h*(w(2,i-1) + k(3,2));
        k(4,2) = h*f(x+h, w(1,i-1)+k(3,1), w(2,i-1)+k(3,2))

        w(1,i) = w(1,i-1) + (1/6)*(k(1,1) + 2*k(2,1) + 2*k(3,1) + k(4,1));
        w(2,i) = w(2,i-1) + (1/6)*(k(1,2) + 2*k(2,2) + 2*k(3,2) + k(4,2));

        kp(1,1) = h*u(2);
        kp(1,2) = h*( f )
    end
end